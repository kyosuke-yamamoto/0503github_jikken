import random

職業リスト = ["戦士", "魔法使い", "僧侶", "盗賊", "騎士", "召喚士"]

職業別ステータス = {
    "戦士":    {"HP": (80, 120), "攻撃力": (15, 25), "魔力": (1,  5),  "素早さ": (5,  10)},
    "魔法使い": {"HP": (40,  60), "攻撃力": (3,   7), "魔力": (20, 35), "素早さ": (8,  14)},
    "僧侶":    {"HP": (55,  75), "攻撃力": (5,  10), "魔力": (15, 25), "素早さ": (6,  12)},
    "盗賊":    {"HP": (50,  70), "攻撃力": (10, 18), "魔力": (3,   8), "素早さ": (15, 25)},
    "騎士":    {"HP": (90, 130), "攻撃力": (12, 20), "魔力": (1,   4), "素早さ": (3,   8)},
    "召喚士":  {"HP": (45,  65), "攻撃力": (5,  10), "魔力": (18, 30), "素早さ": (7,  13)},
}

武器リスト = ["鉄の剣", "魔法の杖", "聖なる槍", "毒の短剣", "炎の弓", "竜の盾", "雷の斧"]

称号リスト = ["勇敢なる", "臆病な", "伝説の", "呪われし", "栄光の", "謎めいた", "無敵の"]

名前候補 = [
    "アルテア", "リオン", "セイラ", "ガイウス", "ミレイ",
    "ゾルダン", "エリナ", "ファルコ", "シオン", "ナディア",
]

def キャラクター生成():
    名前 = random.choice(名前候補)
    職業 = random.choice(職業リスト)
    ステータス範囲 = 職業別ステータス[職業]
    ステータス = {
        能力: random.randint(最小, 最大)
        for 能力, (最小, 最大) in ステータス範囲.items()
    }
    武器 = random.choice(武器リスト)
    称号 = random.choice(称号リスト)
    return 名前, 職業, ステータス, 武器, 称号

def 表示(名前, 職業, ステータス, 武器, 称号):
    print("=" * 30)
    print(f"  名前：{称号}{名前}")
    print(f"  職業：{職業}")
    print(f"  武器：{武器}")
    print("-" * 30)
    for 能力, 値 in ステータス.items():
        print(f"  {能力}：{値}")
    print("=" * 30)

if __name__ == "__main__":
    print("RPGキャラクタージェネレーター")
    print()
    while True:
        名前, 職業, ステータス, 武器, 称号 = キャラクター生成()
        表示(名前, 職業, ステータス, 武器, 称号)
        print()
        続ける = input("もう一度生成しますか？ (y/n): ")
        if 続ける.lower() != "y":
            break
    print("終了します。")
